
public class Manzana {
	private int precio;
	private int peso;

	public Manzana(int precio, int peso) {
		super();
		this.precio = precio;
		this.peso = peso;
	}
	public int getPrecio() {
		return precio;
	}
	public void setPrecio(int precio) {
		this.precio = precio;
	}
	public int getPeso() {
		return peso;
	}
	public void setPeso(int peso) {
		this.peso = peso;
	}

}
