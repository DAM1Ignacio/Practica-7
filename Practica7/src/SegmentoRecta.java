
public class SegmentoRecta {
	private int recta;
	private int A;
	private int B;
	
	
	public SegmentoRecta(int recta, int a, int b) {
		this.recta = recta;
		A = a;
		B = b;
	}
	
	public int getRecta() {
		return recta;
	}
	public void setRecta(int recta) {
		this.recta = recta;
	}
	public int getA() {
		return A;
	}
	public void setA(int a) {
		A = a;
	}
	public int getB() {
		return B;
	}
	public void setB(int b) {
		B = b;
	}

}
